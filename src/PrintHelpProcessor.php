    public function processMessageChain(MessageChain $messageChain, ProgressUpdateCallback $progressUpdateCallback): ProcessingResult
    {
        $message = InternalMessage::asResponseTo($messageChain->last(), $this->buildMarkdown());
        $message->parseMode = 'RichMarkdown';

        return new ProcessingResult($message, true);
    }

    private function buildMarkdown(): string
    {
        $markdown = self::INTRO . "\n\n";
        foreach (self::SECTIONS as $section) {
            $markdown .= '## ' . $section['title'] . "\n\n";
            $markdown .= "| Команда | Описание |\n";
            $markdown .= "| --- | --- |\n";
            foreach ($section['commands'] as $command => $description) {
                $markdown .= '| **' . self::escapeTableCell($command) . '** | '
                    . self::escapeTableCell($description) . " |\n";
            }
            $markdown .= "\n";
        }
        $markdown .= self::CLOSING_NOTE;

        return trim($markdown);
    }

    private static function escapeTableCell(string $content): string
    {
        return str_replace('|', '\|', $content);
    }
